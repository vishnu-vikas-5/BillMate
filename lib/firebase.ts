import AsyncStorage from '@react-native-async-storage/async-storage';
import { getApps, initializeApp } from 'firebase/app';
import * as FirebaseAuth from 'firebase/auth';
import { getFirestore } from 'firebase/firestore';
import { Platform } from 'react-native';

const firebaseConfig = {
  apiKey: process.env.EXPO_PUBLIC_FIREBASE_API_KEY,
  authDomain: process.env.EXPO_PUBLIC_FIREBASE_AUTH_DOMAIN,
  projectId: process.env.EXPO_PUBLIC_FIREBASE_PROJECT_ID,
  storageBucket: process.env.EXPO_PUBLIC_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.EXPO_PUBLIC_FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.EXPO_PUBLIC_FIREBASE_APP_ID,
};

const requiredFirebaseKeys = [
  'EXPO_PUBLIC_FIREBASE_API_KEY',
  'EXPO_PUBLIC_FIREBASE_AUTH_DOMAIN',
  'EXPO_PUBLIC_FIREBASE_PROJECT_ID',
  'EXPO_PUBLIC_FIREBASE_APP_ID',
] as const;

export const missingFirebaseEnvKeys = requiredFirebaseKeys.filter(
  (key) => !process.env[key]
);

export const firebaseConfigured = missingFirebaseEnvKeys.length === 0;

const app = getApps().length === 0 ? initializeApp(firebaseConfig) : getApps()[0];

function createAuth() {
  const getAuth = FirebaseAuth.getAuth;
  const initializeAuth = FirebaseAuth.initializeAuth;
  const getReactNativePersistence = (FirebaseAuth as { getReactNativePersistence?: (storage: typeof AsyncStorage) => unknown }).getReactNativePersistence;

  if (Platform.OS === 'web') {
    return getAuth(app);
  }

  try {
    if (typeof getReactNativePersistence === 'function') {
      return initializeAuth(app, {
        persistence: getReactNativePersistence(AsyncStorage) as never,
      });
    }

    return initializeAuth(app);
  } catch {
    try {
      return getAuth(app);
    } catch {
      return null;
    }
  }
}

export const auth = createAuth();
export const db = getFirestore(app);